package com.java.kalpesh.operators;

public class RelationalOperators {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		System.out.println("a : " + a);
		System.out.println("b : " + b);

		System.out.println("== : " + (a == b));
		System.out.println("!= : " + (a != b));
		System.out.println("> : " + (a > b));
		System.out.println("< : " + (a < b));
		System.out.println(">= : " + (a >= b));
		System.out.println("<= : " + (a <= b));

	}
}
